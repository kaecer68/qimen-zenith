/**
 * 根據 ID 獲取教學章節
 */
export function getTeachingSection(id: string): TeachingSection | undefined {
  return TEACHING_SECTIONS.find(section => section.id === id);
}

/**
 * 獲取所有教學章節（按順序）
 */
export function getAllTeachingSections(): TeachingSection[] {
  return [...TEACHING_SECTIONS].sort((a, b) => a.order - b.order);
}
